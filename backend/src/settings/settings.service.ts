@Injectable()
export class SettingsService {
  constructor(private prisma: PrismaService) {}

  async getSettings() {
    const settings = await this.prisma.adminSettings.findUnique({
      where: { id: 'default' },
    });
    return settings?.settings || DEFAULT_SETTINGS;
  }

  async updateSettings(data: any) {
    const existing = await this.prisma.adminSettings.findUnique({
      where: { id: 'default' },
    });

    if (existing) {
      await this.prisma.adminSettings.update({
        where: { id: 'default' },
        data: { settings: data },
      });
    } else {
      await this.prisma.adminSettings.create({
        data: { id: 'default', settings: data },
      });
    }

    return data;
  }
}
